class Solution {
public:
    int longestPalindromeSubseq(string s) {
        vector<vector<int>> dp(s.size(), vector<int>(s.size(), 0));
        for (int i = 0; i < s.size(); ++i) dp[i][i] = 1;//åˆå§‹åŒ–
        for (int i = s.size() - 1; i >= 0; --i) {
            for (int j = i + 1; j < s.size(); ++j) {
                dp[i][j] = (s[i] == s[j])
                           ? dp[i + 1][j - 1] + 2
                           : max(dp[i][j - 1], dp[i + 1][j]);
            }
        }
        return dp[0].back();
    }
};
