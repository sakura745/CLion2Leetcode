class Solution {
    vector<int> path;
    vector<vector<int>> res;
    void backtracking(vector<int>& candidates, int target, int startIndex) {
        if (target == 0) {
            res.push_back(path);
            return;
        }
        for (int i = startIndex; i < candidates.size() && target >= 0/*å‰ªæ*/; ++i) {
            path.push_back(candidates[i]);
            backtracking(candidates, target - candidates[i]/*å›æº¯*/, i/*ä¸‹ä¸€ç»„æ•°å­—å–å€¼èŒƒå›´ç”±å®ƒæ§åˆ¶*/);
            path.pop_back();//å›æº¯
        }
    }
public:
    vector<vector<int>> combinationSum(vector<int>& candidates, int target) {
        backtracking(candidates, target, 0);
        return res;
    }
};
