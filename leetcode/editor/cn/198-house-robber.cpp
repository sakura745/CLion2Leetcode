class Solution {
public:
    int rob(vector<int>& nums) {
        if (nums.size() == 1) return nums[0];
        vector<int> dp{nums[0], max(nums[0], nums[1])};
        dp.resize(nums.size());//不是nums.size() + 1，是因为dp是按照nums对应求得的
        for (int i = 2; i < nums.size(); ++i) {
            dp[i] = max(dp[i - 1], dp[i - 2] + nums[i]);
        }
        return dp.back();
    }
};
