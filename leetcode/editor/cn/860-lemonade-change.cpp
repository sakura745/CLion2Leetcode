class Solution {
public:
    bool lemonadeChange(vector<int>& bills) {
        int wallet[2] = {};
        for (auto& bill : bills) {
            if (bill == 5) ++wallet[0];
            if (bill == 10) {
                if (wallet[0] == 0) {
                    return false;
                }
                ++wallet[1];
                --wallet[0];
            }
            if (bill == 20) {
                if (wallet[0] > 0 && wallet[1] > 0) {//先花10
                    --wallet[0];
                    --wallet[1];
                } else if (wallet[0] >= 3) {//10不够再花5
                    wallet[0] -= 3;
                } else
                    return false;
            }
        }
        return true;
    }
};
