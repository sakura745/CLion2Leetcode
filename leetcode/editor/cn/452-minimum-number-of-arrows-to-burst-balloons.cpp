class Solution {
public:
    int findMinArrowShots(vector<vector<int>>& points) {
        sort(points.begin(), points.end()
             /*, [](const auto& lhs, const auto& rhs) {
            return lhs[0] < rhs[0];
        }*/);
        int res = 1;
        for (int i = 1; i < points.size(); ++i) {
            if (points[i - 1][1] < points[i][0]) {
                ++res;
            } else {
                //更新i的右区间，为下一个遍历使用
                points[i][1] = min(points[i - 1][1], points[i][1]);
            }
        }
        return res;
    }
};
