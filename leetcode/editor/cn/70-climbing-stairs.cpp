class Solution {
public:
    int climbStairs(int n) {
        vector<int> dp{1, 1, 2};
        dp.resize(n + 1);
        for (int i = 3; i < n + 1; ++i) {
            dp[i] = dp[i - 2] + dp[i - 1];
        }
        return dp[n];
    }
};
