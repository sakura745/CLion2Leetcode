class Solution {
public:
    int minCostClimbingStairs(vector<int>& cost) {
        vector<int> dp{0, 0};
        dp.resize(cost.size() + 1);
        for (int i = 2; i < cost.size() + 1; ++i) {
            dp[i] = min(dp[i - 1] + cost[i - 1], dp[i - 2] + cost[i - 2]);
        }
        return dp.back();
    }
};
