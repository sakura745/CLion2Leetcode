class Solution {
public:
    int jump(vector<int>& nums) {
        if (nums.size() == 1) return 0;
        int cur = 0, next = 0;
        int res = 0;
        for (int i = 0; i < nums.size() - 1; ++i) {
            next = max(i + nums[i], next);
            if (i == cur/*可以执行到此处，i == cur，证明cur< nums.size() - 1*/) {
                res++;
                cur = next;
            }
        }
        return res;
    }
};
