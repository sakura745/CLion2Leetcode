class Solution {
public:
    int largestRectangleArea(vector<int>& heights) {
        heights.push_back(0);//æ•°ç»„å°¾éƒ¨åŠ 0
        stack<int> st;
        int sum = 0;
        for (int i = 0; i < heights.size(); ++i) {
            while (!st.empty() && heights[i] < heights[st.top()]) {
                int cur = st.top();
                st.pop();
                int Width = st.empty() ? i/*å½“æ ˆä¸ºç©ºæ—¶ï¼Œç›´æ¥è¾“å‡ºiä½œä¸ºå®½åº¦*/ : i - st.top() - 1;
                sum = max(sum, Width * heights[cur]);
            }
            st.push(i);
        }
        return sum;
    }
};
