class Solution {
public:
    int trap(vector<int>& height) {
        stack<int> st;
        int sum = 0;
        for (int i = 0; i < height.size(); ++i) {
            while (!st.empty() && height[i] > height[st.top()]) {
                int cur = st.top();
                st.pop();

                //å…ˆå–å‡ºå½“å‰å…ƒç´ curï¼Œå¼¹å‡ºï¼Œæ‰èƒ½è·å¾—å¤§äºå½“å‰å…ƒç´ çš„å·¦ä¾§å…ƒç´ ã€‚
                if (!st.empty()/*ç¡®ä¿æœ‰å·¦ä¾§å…ƒç´ */) {
                    //æ±‚é¢ç§¯
                    int Height = min(height[st.top()/*å·¦ä¾§å…ƒç´ */], height[i/*å³ä¾§å…ƒç´ */]) - height[cur];
                    int Width = i - st.top() - 1;
                    sum += Height * Width;
                }
            }
            st.push(i);
        }
        return sum;
    }
};
