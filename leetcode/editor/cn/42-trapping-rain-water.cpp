class Solution {
public:
    int trap(vector<int>& height) {
        stack<int> st;
        int sum = 0;
        for (int i = 0; i < height.size(); ++i) {
            while (!st.empty() && height[i] > height[st.top()]) {
                int cur = st.top();
                st.pop();

                //先取出当前元素cur，弹出，才能获得大于当前元素的左侧元素。
                if (!st.empty()/*确保有左侧元素*/) {
                    //求面积
                    int Height = min(height[st.top()/*左侧元素*/], height[i/*右侧元素*/]) - height[cur];
                    int Width = i - st.top() - 1;
                    sum += Height * Width;
                }
            }
            st.push(i);
        }
        return sum;
    }
};
