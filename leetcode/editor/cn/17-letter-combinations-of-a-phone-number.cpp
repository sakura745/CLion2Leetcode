class Solution {
public:
    vector<string> telephoto{"", "", "abc" ,"def", "ghi", "jkl", "mno", "pqrs", "tuv", "wxyz"};
    vector<string> res;
    void backtracking(const string& digits, int index, const string& path) {
        if (index == digits.size()/*æ·±åº¦*/) {
            res.push_back(path);
            return;
        }
        int tmp = digits[index] - '0';//å–å‡ºdigitsä¸­å¯¹åº”çš„æ•°å­—
        for (auto& i : telephoto[tmp]/*å®½åº¦*/) {
            backtracking(digits, index + 1/*ç»ˆæ­¢æ¡ä»¶å’Œå»é‡*/, path + i/*å›æº¯*/);
        }
    }
    vector<string> letterCombinations(string digits) {
        if (digits.empty()) return res;
        backtracking(digits, 0, "");
        return res;
    }
};
