class Solution {
public:
    int numTrees(int n) {
        vector<int> dp{1};
        dp.resize(n + 1);
        for (int i = 1; i < n + 1; ++i) {
            //æ±‚å’Œçš„é€’æ¨å…¬å¼
            for (int j = 1; j < i + 1; ++j) {
                dp[i] += dp[j - 1] * dp[i - j];
            }
        }
        return dp.back();
    }
};
