class Solution {
public:
    int findTargetSumWays(vector<int>& nums, int target) {
        int sum = accumulate(nums.begin(), nums.end(), 0);
        if (abs(target) > sum/*target超过sum*/ || (sum + target) % 2 == 1/*sum、target和不能被2整除，表示不存在*/) return 0;
        vector<int> dp{1};
        dp.resize((sum + target) / 2 + 1);
        for (int i = 0; i < nums.size(); ++i) {
            for (int j = (sum + target) / 2; j >= nums[i]; --j) {
                dp[j] += dp[j - nums[i]];
            }
        }
        return dp.back();
    }
};
