class Solution {
public:
    vector<int> path;
    vector<vector<int>> res;
    void backtracking(int n, int k, int startIndex) {
        if (path.size() == k) {
            res.push_back(path);
            return;
        }
        for (int i = startIndex; i <= n - (k - path.size()) + 1/*å‰ªæ*/; ++i) {
            path.push_back(i);
            backtracking(n, k, i + 1/*å¾ªç¯å†…ç”¨i*/);
            path.pop_back();//å›æº¯
        }
    }
    vector<vector<int>> combine(int n, int k) {
        backtracking(n, k, 1);
        return res;
    }
};
