class Solution {
public:
    class cmp {//æ¯”è¾ƒå‡½æ•°
    public:
        bool operator()(const pair<int, int>& lhs, const pair<int, int>& rhs) {
            return rhs.second < lhs.second;
        }
    };

    vector<int> topKFrequent(vector<int>& nums, int k) {
        unordered_map<int, int> map;
        vector<int> res;
        for (int i = 0; i < nums.size(); ++i) {
            map[nums[i]]++;//key æ•°å­—ï¼Œvalueå‡ºç°çš„æ¬¡æ•°
        }

        priority_queue<pair<int, int>, vector<pair<int, int>>, cmp> que;//å°æ ¹å †

        for (auto it = map.begin(); it != map.end(); ++it) {//éå†map
            que.push(*it);//åŠ å…¥å°æ ¹å †
            if (que.size() > k) {//ä¿ç•™å‰kä¸ª
                que.pop();
            }
        }

        while (!que.empty()) {
            res.push_back(que.top().first);
            que.pop();
        }
        return res;
    }
};
